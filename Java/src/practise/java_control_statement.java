package practise;

public class java_control_statement {

	public static void main(String[] args) {
		//1)Decision making statement
		//a)i)if STATEMENT
		
		int a = 70;
		if(a<50) {
			System.out.println("a is less than 50");
			
		}
		
		int b = 100;
		if(b<=200) {
			System.out.println("b is less than 200");
			// Here we got the result when only the condition / block of code is true.
		}
		
		//ii) If-else statement
		int p = 50;
		if(p>=200) {
			System.out.println("p is less than 200");
		}else {
			System.out.println("value of p is greater than 200");
			
				
			}
		
	}

}
